let age = '20'

if (age == 20) { // * Neste caso, se o valor comparado for igual a '20', a condição resultará em "true"
    console.log('You are 20 years old.');
}
if (age === 20) {
    console.log('You really are 20 years old.'); // * Usando ===, além de comparar o valor propriamente dito, o tipo do dado também é comparado
}